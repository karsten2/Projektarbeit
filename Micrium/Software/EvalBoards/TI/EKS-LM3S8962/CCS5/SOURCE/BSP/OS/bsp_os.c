/*
*********************************************************************************************************
*                                          BSP_OS_Dly_ms()
*
* Description : Delay for specified time, in milliseconds.
*
* Argument(s) : ms      Time delay value, in milliseconds.
*
* Return(s)   : none.
*
* Caller(s)   : various.
*
* Note(s)     : none.
*********************************************************************************************************
*/

void  BSP_OS_Dly_ms (CPU_INT16U  ms)
{
    OS_ERR   err;

    OSTimeDlyHMSM(0u ,0u , 0u, ms, OS_OPT_TIME_HMSM_STRICT, &err );
}
